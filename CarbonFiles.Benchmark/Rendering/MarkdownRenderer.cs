using System.Text;

namespace CarbonFiles.Benchmark.Rendering;

public static class MarkdownRenderer
{
    public static string Render(string url, DateTime startedAt, List<BenchmarkResult> results)
    {
        var sb = new StringBuilder();

        // Header
        sb.AppendLine("# CarbonFiles Benchmark Report");
        sb.AppendLine();
        sb.AppendLine($"- **Target:** `{url}`");
        sb.AppendLine($"- **Started:** {startedAt:u}");
        sb.AppendLine($"- **Completed:** {DateTime.UtcNow:u}");
        sb.AppendLine();

        // Scorecard summary
        var total = results.Count;
        var passed = results.Count(r => r.Success);
        var failed = total - passed;

        sb.AppendLine("## Summary");
        sb.AppendLine();
        sb.AppendLine($"| Metric | Value |");
        sb.AppendLine($"|--------|-------|");
        sb.AppendLine($"| Total Benchmarks | {total} |");
        sb.AppendLine($"| Passed | {passed} |");
        sb.AppendLine($"| Failed | {failed} |");

        var allLatencies = results
            .Where(r => r.Success && r.LatenciesMs.Count > 0)
            .SelectMany(r => r.LatenciesMs)
            .Where(l => l >= 0)
            .ToList();

        if (allLatencies.Count > 0)
        {
            allLatencies.Sort();
            sb.AppendLine($"| Global Median | {Formatting.FormatMs(Statistics.Median(allLatencies))} |");
            sb.AppendLine($"| Global P95 | {Formatting.FormatMs(Statistics.P95(allLatencies))} |");
            sb.AppendLine($"| Global P99 | {Formatting.FormatMs(Statistics.P99(allLatencies))} |");
        }

        sb.AppendLine();

        // Per-category tables
        sb.AppendLine("## Results");
        sb.AppendLine();

        var categories = results.Select(r => r.Category).Distinct().ToList();

        foreach (var category in categories)
        {
            sb.AppendLine($"### {category}");
            sb.AppendLine();
            sb.AppendLine("| Operation | Min | Median | P95 | P99 | Max | Ops/s | Throughput | Status |");
            sb.AppendLine("|-----------|----:|-------:|----:|----:|----:|------:|-----------:|:------:|");

            var categoryResults = results.Where(r => r.Category == category).ToList();
            foreach (var r in categoryResults)
            {
                var status = r.Success ? "PASS" : "FAIL";

                if (!r.Success)
                {
                    sb.AppendLine($"| {r.Operation} | - | - | - | - | - | - | - | {status} |");
                    continue;
                }

                var sorted = r.LatenciesMs;
                var has = sorted.Count > 0;

                sb.AppendLine(string.Join(" | ",
                    $"| {r.Operation}",
                    has ? Formatting.FormatMs(Statistics.Min(sorted)) : "-",
                    has ? Formatting.FormatMs(Statistics.Median(sorted)) : "-",
                    has ? Formatting.FormatMs(Statistics.P95(sorted)) : "-",
                    has ? Formatting.FormatMs(Statistics.P99(sorted)) : "-",
                    has ? Formatting.FormatMs(Statistics.Max(sorted)) : "-",
                    has ? Formatting.FormatOps(Statistics.OpsPerSec(sorted)) : "-",
                    Formatting.FormatThroughput(r.ThroughputMbPerSec),
                    $"{status} |"
                ));
            }

            sb.AppendLine();
        }

        // Failures
        var failures = results.Where(r => !r.Success).ToList();
        if (failures.Count > 0)
        {
            sb.AppendLine("## Failures");
            sb.AppendLine();
            foreach (var f in failures)
            {
                sb.AppendLine($"- **{f.Category}** / {f.Operation}");
                if (f.Error != null)
                    sb.AppendLine($"  - `{f.Error}`");
            }
            sb.AppendLine();
        }

        sb.AppendLine("---");
        sb.AppendLine("*Generated by [CarbonFiles Benchmark Suite](https://github.com/carbungo/carbon-files-bench)*");

        return sb.ToString();
    }
}
